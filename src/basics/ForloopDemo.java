package basics;

public class ForloopDemo {
    public static void main(String[] args) {
        System.out.println("Before LOOP");

        for(int i=0;i<5;i++)
        {
            System.out.println("Value is: "+i);
            System.out.println("Inside Loop");
        }

        System.out.println("After LOOP");
    }
}
